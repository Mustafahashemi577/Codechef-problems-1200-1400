import java.util.*;
import java.lang.*;
import java.io.*;

class Sarthak_And_His_Magic_Grid
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner scan = new Scanner(System.in);
	
		int t = scan.nextInt();
		
		while(t-- > 0 ){
		    int n = scan.nextInt();
		    
		    for (int i = 0 ; i < n ; i++ ) {
		        for (int j = 0 ; j < n ; j++ ) {
		            System.out.print( 1 + " " );
		        }
		        System.out.println();
		    }
		    
		}

	}
}
